                    <h2>Background</h2>
                    <p>On 1st October 2003, the Information and Communications Technology Authority, now OfReg (also the 'Office') adopted the Uniform Domain-Name Dispute-Resolution Policy (often referred to as the ‘UDRP’), as published by the Internet Corporation for Assigned Names and Numbers (‘ICANN’), as the basis upon which it resolves all disputes relating to the registration of .ky domain names.</p>
                    <h2>Resolving Disputes</h2>
                    <p>Before the Office considers any .ky domain registration dispute, there should first have been good faith attempts made between the person bringing the dispute (the ‘Complainant’) and the person who registered the domain name in dispute (the ‘Registrant’) to resolve the matter; evidence of which will need to be provided to the Office when any complaint is brought to it.</p>
                    <p>On the basis that such ‘good faith’ attempts have been made, and that the subject of the dispute is still unresolved, the Complainant can bring the dispute to the Office for the Office to decide, based on the UDRP, whether or not, among other things, the .ky domain name in dispute should be transferred to the Complainant.</p>
                    <p>In bringing the dispute, the Complainant should submit a complaint to the Office and in that complaint reference how the UDRP applies to the facts of the dispute.  In particular, the Complaint should include submissions and evidence explaining why it considers that the dispute is an “Applicable Dispute” and the “Registration and Use [of the domain name in dispute is] in Bad Faith” under that policy.</p>
                    <p>Please click here to download the  KY Domain Complaint Form</p>
                    <p>In this regard, for example, the Complainant may want to provide the Office with copies of relevant trademarks, invoices, screen shots, communication (letters and emails) and other evidence that support its submissions.  If the Complainant submits that it has a ‘reputation’ in the .ky domain name, the Office will need proof of this.</p>
                    <p> Also, the Complainant should make it clear whether or not the dispute is being brought by it as an individual, company or on behalf of someone else.  If the Complainant is bringing the dispute on behalf of someone else, it should provide supporting documents that it has the appropriate consent to do so.</p>
                    <blockquote>                        
                        <p>If you are the Registrant, when submitting a response to a complaint, you should reference how the UDRP applies to the facts of the dispute.  In particular, the Registrant should include submissions and evidence explaining why it has “Rights to and Legitimate Interest in the Domain Name.”</p>
                    </blockquote>
