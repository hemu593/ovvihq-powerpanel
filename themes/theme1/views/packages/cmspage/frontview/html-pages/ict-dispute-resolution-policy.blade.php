                            <h2>Timetable</h2>
                            <p>As to the timetable for consideration of the complaint made by the Complainant, noting that the Office needs to ensure that the dispute resolution process is fair to both sides:</p>
                            <ul>
                                <li>on receipt of the complaint, the Office will send the complaint to the Registrant and give twenty (20) days for a response; and,</li>
                                <li>if a response is submitted by the Registrant, that response will be sent to the Complainant who will be given ten (10) days in which to submit a reply.</li>
                            </ul>
