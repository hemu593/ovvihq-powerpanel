                            <h2>Mediation</h2>
                            <p>Once the Office has received the submissions referenced above, a trained member of our team will speak neutrally and confidentially to both parties, to seek to facilitate an acceptable resolution of the dispute.</p>
                            <p>Any negotiations conducted between the parties during this stage (including any information obtained from or in connection to such negotiations) will be treated as confidential by the Office.</p>
                            <p>If the case does not settle at mediation within ten (10) days, the Office will offer the Complainant the chance to request that the Office make a formal decision.</p>
