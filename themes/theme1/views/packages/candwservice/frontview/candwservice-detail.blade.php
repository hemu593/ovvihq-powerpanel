                        <h4>CIVIL SERVICE AND PRIVATE SECTOR CAREERS</h4>
                        <p>Dr. Pierson worked for 16 years in the Cayman Islands Civil Service (1963-1979), where he filled a number of senior positions, including Principal Secretary (now Chief Officer) for Health, Education, and Social Services, and Deputy Financial Secretary (acting as Financial Secretary on several occasions). During the 1980s he was employed in various private sector positions, including Director of Finance for Cayman Airways, local Partner of KPMG/Thorne Riddell (the then Canadian Chartered Accounting Firm), and he held Directorships in a number of private companies.</p>
                        <h4>MINISTER AND SPEAKER OF THE LEGISLATIVE ASSEMBLY</h4>
                        <p>Dr. Pierson represented the District of George Town for 16 plus years (1984-1988; 1988-1992; 1996-2000; 2000-2005) as an elected Member of the Legislative Assembly. During his political career he filled the position of Cabinet Minister for seven years. In his first term as a Minister (1988-1992), he was responsible for a number of utilities, including: The Water Authority; Cayman Water Company; Electricity Regulatory Authority; Petroleum and Fuel supply stakeholders; Information and Communication Technology Authority—he was the founding Minister for this Authority. He was also responsible for Public Health Department; Public Works Department (now National Roads Authority) among his other ministerial responsibilities. Dr. Pierson also completed a number of important projects, including the Cayman Islands Building Code, upgrade of the telecommunications system to accommodate the 911 system within the Cayman Islands, installation of the Bulk Storage Fuel Terminal in Cayman Brac, and establishment of the first Public Water Supply System in Cayman Brac.</p>
                        <p>In his second term as a Cabinet Minister (2000-2003), among the number of projects which he spearheaded, Dr. Pierson successfully negotiated the liberalization of the telecommunications sector, which had hitherto been monopolized by the single telecommunications company (Cable & Wireless). In 2003 Dr. Pierson was elected as Speaker of the Legislative Assembly, and served in that position until his retirement from active politics in March, 2005. In addition to his combined public service, which spanned approximately 33 years (16 years in the Civil Service, and 16 plus years as a political representative), he assisted in a number of Civic/Community Services. As a Rotarian for 47 years (1968-2015), Dr. Pierson served as President of the Rotary Club of Grand Cayman Central (1988/1989), and Assistant Governor in District 7020 of Rotary International (2004/2005).</p>
                        <h4>CHAIR OF THE UTILITY REGULATION AND COMPETITION OFFICE</h4>
                        <p>Dr. Pierson was appointed as the first Chairman of the Board of Directors of OfReg in January 2017 for a three-year term, and was re-appointed for a further three-year term in January 2020. He leads the Board of OfReg in their commitment to attain regulatory excellence, and to ensure safe, reliable, economic public utilities to businesses and the people of the Cayman Islands. In his Foreword to OfReg’s first Annual Report for 2017, he stated that “initial success would not have been achieved without the dedication, attention to duty and the tenacity of the Executive team led by the Chief Executive Officer, Mr. J. Paul Morgan” (of blessed memory).</p>
