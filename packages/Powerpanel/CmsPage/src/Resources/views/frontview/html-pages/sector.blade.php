                        <h3 class="nqtitle n-mb-lg-30">What we do</h3>
                        <p>We develop, administer, and enforce the regulatory framework that governs nearly every aspect of the Cayman Islands’ electricity industry including transmission and distribution, system operation, security of supply, safety, reliability, and service.</p>
                        <p>We analyse and monitor the performance of the market and the electricity industry and make the information available on our website. We help develop the industry through education, guidelines and rules, information, regulations, and innovation arrangements.
                            <p>Our usual work focuses on addressing key questions that affect the long-term benefit of electricity consumers. This work includes reviews and approves rates, tariffs, charges, and fees, determination of the allowable rate of earnings in establishing rates and acts on applications for the acquisition, sale, disposition, or other exchange of utility properties, including mergers and consolidations.</p>
                            <p>Additionally, OfReg tracks incidents, such as voltage dips, that affect supply voltages to ensure power quality is maintained.</p>
