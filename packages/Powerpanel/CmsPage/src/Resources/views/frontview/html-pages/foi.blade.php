                        <h3 class="nqtitle n-mb-lg-30">Commercially Sensitive Information</h3>
                        <p>One exemption of relevance to OfReg and its licensees covers commercially sensitive information. The Office handles commercially sensitive information under Section 107 of the Utility Regulation and Competition Act (2021 Revision) (the “URC Act”) or the Information & Communications Technology Authority (Confidentiality) Regulations (“the Confidentiality Regulations”). Under Section 107(3) of the URC Act, OfReg shall grant a request to treat information as confidential if OfReg concludes that the information is:</p>
                        <ul>
                            <li>A trade secret of any person</li>
                            <li>Information, the commercial value of which would be, or could reasonably be expected to be destroyed or diminished by disclosure</li>
                            <li>Other information, the disclosure of which would have, or could reasonably be expected to have, an adverse effect on the commercial interests of any person to whom the information relates</li>
                            <li>Information:
                                <ul>
                                    <li>that is given to OfReg by a third party (other than another sectoral utility) in confidence on the understanding that it would be treated as confidential;</li>
                                    <li>the disclosure of which would be likely to prevent OfReg from receiving further similar information required by OfReg to fulfil its functions properly; or</li>
                                    <li>Information, the disclosure of which would constitute a breach of a duty of confidence provided for by a provision of an Act.”</li>
                                </ul>
                            </li>
                        </ul>
